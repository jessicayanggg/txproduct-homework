function sumOfSquareAreas(a, b) {
    return (a*a) + (b*b);
}

sumOfSquareAreas(9, 3)
sumOfSquareAreas(2, 7)
sumOfSquareAreas(5, 10)

function gradeCalculator(homeworkAvg, quizAvg, examAvg) {
    return (homeworkAvg * 0.10) + (quizAvg * 0.15) + (examAvg * .75);
}

gradeCalculator(90, 85, 76)
gradeCalculator(100, 67, 92)
gradeCalculator(95, 100, 73)

